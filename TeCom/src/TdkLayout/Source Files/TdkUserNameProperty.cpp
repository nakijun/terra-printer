#include<TdkLayoutTypes.h>
#include <TdkUserNameProperty.h>
#include <TdkLayoutDatabaseObject.h>


///////////////////////////////////////////////////////////////////////////////
//Constructor
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkUserNameProperty::TdkUserNameProperty(TdkLayoutDatabaseObject *obj)
{
	_propertyName="UserName_Property";
	_name.clear();
	_type=PText;
	_object=obj;
}


///////////////////////////////////////////////////////////////////////////////
//Destructor
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkUserNameProperty::~TdkUserNameProperty()
{


}

///////////////////////////////////////////////////////////////////////////////
//Sets user name value
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
void TdkUserNameProperty::setValue(const std::string &newVal)
{
	_name=newVal;
	if(_object) _object->closeConnection();
}


///////////////////////////////////////////////////////////////////////////////
//Returns the user name value
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////

std::string TdkUserNameProperty::getValue()
{
	return _name;
}


///////////////////////////////////////////////////////////////////////////////
//Returns the user name value by reference
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
void TdkUserNameProperty::getValue(std::string &value)
{
	value=_name;
}

///////////////////////////////////////////////////////////////////////////////
//Operator =
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkUserNameProperty& TdkUserNameProperty::operator=(const TdkUserNameProperty &other)
{
	_name=other._name;
	return *this;
}

///////////////////////////////////////////////////////////////////////////////
//Operator =
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkUserNameProperty& TdkUserNameProperty::operator=(const TdkAbstractProperty &other)
{
	_name=((TdkUserNameProperty&)other)._name;
	return *this;
}