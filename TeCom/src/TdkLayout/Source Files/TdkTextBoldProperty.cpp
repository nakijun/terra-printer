TdkTextBoldProperty::TdkTextBoldProperty(const bool &newVal)
{
	_propertyName="TextBold_Property";
	_value=newVal;
	_type=PBoolean;
}

///////////////////////////////////////////////////////////////////////////////
//Destructor
//Author : Rui Mauricio Gregório 
//Date : 07/2009
///////////////////////////////////////////////////////////////////////////////

TdkTextBoldProperty::~TdkTextBoldProperty()
{

}

///////////////////////////////////////////////////////////////////////////////
//Sets Text Bold status
//Author : Rui Mauricio Gregório 
//Date : 07/2009
///////////////////////////////////////////////////////////////////////////////

void TdkTextBoldProperty::setValue(const bool &newVal)
{
	_value=newVal;
}

///////////////////////////////////////////////////////////////////////////////
//Returns the text bold status
//Author : Rui Mauricio Gregório 
//Date : 07/2009
///////////////////////////////////////////////////////////////////////////////

bool TdkTextBoldProperty::getValue()
{
	return _value;
}

///////////////////////////////////////////////////////////////////////////////
//Returns the text bold status by reference
//Author : Rui Mauricio Gregório 
//Date : 08/2009
///////////////////////////////////////////////////////////////////////////////

void TdkTextBoldProperty::getValue(bool &value)
{
	value=_value;
}

///////////////////////////////////////////////////////////////////////////////
//Equal Operator
//Author : Rui Mauricio Gregório 
//Date : 07/2009
///////////////////////////////////////////////////////////////////////////////

TdkTextBoldProperty& TdkTextBoldProperty::operator=(const TdkTextBoldProperty &other)
{
	_value=other._value;
	return *this;
}

///////////////////////////////////////////////////////////////////////////////
//Equal Operator
//Author : Rui Mauricio Gregório 
//Date : 07/2009
///////////////////////////////////////////////////////////////////////////////

TdkTextBoldProperty& TdkTextBoldProperty::operator=(const TdkAbstractProperty &other)
{
	_value=((TdkTextBoldProperty&)other)._value;
	return *this;
}