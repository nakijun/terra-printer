//Destructor
//Author : Rui Mauricio Gregório 
//Date : 07/2009
///////////////////////////////////////////////////////////////////////////////

TdkTextValueProperty::~TdkTextValueProperty()
{

}

///////////////////////////////////////////////////////////////////////////////
//Sets text Value
//Author : Rui Mauricio Gregório 
//Date : 07/2009
///////////////////////////////////////////////////////////////////////////////

void TdkTextValueProperty::setValue(const std::string &newVal)
{
	_value=newVal;
}

///////////////////////////////////////////////////////////////////////////////
//Returns the text value
//Author : Rui Mauricio Gregório 
//Date : 07/2009
///////////////////////////////////////////////////////////////////////////////

std::string TdkTextValueProperty::getValue()
{
	return _value;
}

///////////////////////////////////////////////////////////////////////////////
//Returns the text value by reference
//Author : Rui Mauricio Gregório 
//Date : 08/2009
///////////////////////////////////////////////////////////////////////////////
void TdkTextValueProperty::getValue(std::string &value)
{
	value=_value;
}

