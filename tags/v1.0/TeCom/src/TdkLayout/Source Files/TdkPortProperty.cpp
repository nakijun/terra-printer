///////////////////////////////////////////////////////////////////////////////
TdkPortProperty::TdkPortProperty()
{
	_propertyName="Port_Property";
	_port.clear();
	_type=PText;
}

///////////////////////////////////////////////////////////////////////////////
//Destructor
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkPortProperty::~TdkPortProperty()
{


}

///////////////////////////////////////////////////////////////////////////////
//Set Value
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
void TdkPortProperty::setValue(const std::string &newVal)
{
	_port=newVal;
}

///////////////////////////////////////////////////////////////////////////////
//Get Value
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
std::string TdkPortProperty::getValue()
{
	return _port;
}

///////////////////////////////////////////////////////////////////////////////
//Returns the port value by reference
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////

void TdkPortProperty::getValue(std::string &value)
{
	value=_port;
}

///////////////////////////////////////////////////////////////////////////////
//Operator
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkPortProperty& TdkPortProperty::operator=(const TdkPortProperty &other)
{
	_port=other._port;
	return *this;
}

///////////////////////////////////////////////////////////////////////////////
//Operator
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkPortProperty& TdkPortProperty::operator=(const TdkAbstractProperty &other)
{
	_port=((TdkPortProperty&)other)._port;
	return *this;
}