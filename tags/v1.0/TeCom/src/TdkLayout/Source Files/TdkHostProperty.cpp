#include<TdkLayoutTypes.h>
#include <TdkHostProperty.h>
#include <TdkLayoutDatabaseObject.h>

///////////////////////////////////////////////////////////////////////////////
//Constructor
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkHostProperty::TdkHostProperty(TdkLayoutDatabaseObject *obj)
{
	_propertyName="Host_Property";
	_host.clear();
	_type=PText;
	_object=obj;
}

///////////////////////////////////////////////////////////////////////////////
//Destructor
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkHostProperty::~TdkHostProperty()
{


}

///////////////////////////////////////////////////////////////////////////////
//Set Value
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
void TdkHostProperty::setValue(const std::string &newVal)
{
	_host=newVal;
	if(_object) _object->closeConnection();
}

///////////////////////////////////////////////////////////////////////////////
//Get Value
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
std::string TdkHostProperty::getValue()
{
	return _host;
}

///////////////////////////////////////////////////////////////////////////////
//Returns the host name value by reference
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
void TdkHostProperty::getValue(std::string &value)
{
	value=_host;
}

///////////////////////////////////////////////////////////////////////////////
//Operator
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkHostProperty& TdkHostProperty::operator=(const TdkHostProperty &other)
{
	_host=other._host;
	return *this;
}

///////////////////////////////////////////////////////////////////////////////
//Operator
//Author : Rui Mauricio Gregório 
//Date : 08/2010
///////////////////////////////////////////////////////////////////////////////
TdkHostProperty& TdkHostProperty::operator=(const TdkAbstractProperty &other)
{
	_host=((TdkHostProperty&)other)._host;
	return *this;
}